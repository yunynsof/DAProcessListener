package hn.com.tigo.da.listener.model;

public class AdditionalProperty {

	private String Value;

	private String Code;

	public String getValue() {
		return Value;
	}

	public void setValue(String value) {
		Value = value;
	}

	public void setCode(String Code) {
		this.Code = Code;
	}

	public String getCode() {
		return this.Code;
	}
}
